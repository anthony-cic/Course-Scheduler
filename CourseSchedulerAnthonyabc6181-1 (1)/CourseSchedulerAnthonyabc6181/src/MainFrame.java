        dropStudentTextArea.setText("");
        ArrayList<StudentEntry> students = StudentQueries.getAllStudents();
        String studentID = students.get(dropStudentComboBox.getSelectedIndex()).getStudentID();
        StudentEntry student = StudentQueries.getStudent(studentID);
        
        // drop student from all courses on all semesters and print results
        // drop from list of students 
        ArrayList<String> semesters = SemesterQueries.getSemesterList(); 
        
        dropStudentTextArea.append(student.getLastName() + ", " + student.getFirstName() + " " + student.getStudentID() + " has been removed from the students list" + "\n");
        
        for (String semester: semesters) {
            dropStudentTextArea.append("For Semester: " + semester + "\n"); 
            ArrayList<ScheduleEntry> schedule = ScheduleQueries.getScheduleByStudent(semester, studentID);
           for (ScheduleEntry studentSchedule: schedule) {
               String course = studentSchedule.getCourseCode();
               String waitlist = studentSchedule.getStatus(); 
               String waitlistString = ""; 
               if ("W".equals(waitlist)) {
                   waitlistString = "the waitlist for ";
                }
               System.out.println(semester + " " + studentID + " " + course);
               dropStudentTextArea.append(student.getLastName() + ", " + student.getFirstName() + " " + student.getStudentID() + " has been dropped from " +  waitlistString + course + "\n");
               ScheduleQueries.dropStudentScheduleByCourse(semester, studentID, studentSchedule.getCourseCode());
               StudentQueries.dropStudent(studentID); 
               
               ArrayList<ScheduleEntry> waitlistedStudents = ScheduleQueries.getWaitlistedStudentsByCourse(semester, course);
   
            if ((waitlistedStudents.size() > 0) && (ScheduleQueries.getScheduledStudentCount(semester, course) < CourseQueries.getCourseSeats(semester, course))) { // If there are students waitlisted for course and space in class 
            
                ScheduleEntry waitlistedStudent = waitlistedStudents.get(0); 
                waitlistedStudent.setStatus("S"); 
                //ScheduleQueries.updateScheduleEntry(semester, waitlistedStudent);
                ScheduleQueries.dropStudentScheduleByCourse(semester, waitlistedStudent.getStudentID(), course);
                StudentEntry waitlistedStudentEntry = StudentQueries.getStudent(waitlistedStudent.getStudentID());
                ScheduleQueries.addScheduleEntry(waitlistedStudent);
                dropStudentTextArea.append(waitlistedStudentEntry.getLastName() + ", " + waitlistedStudentEntry.getFirstName() + " " + waitlistedStudentEntry.getStudentID() + " has been added into " + course + "\n");
                }   
            } 
            
        }

        //StudentQueries.dropStudent(studentID); 
        rebuildStudentComboBox(); 
               
         
        

