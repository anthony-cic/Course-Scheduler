    private void adminDropCourseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adminDropCourseButtonActionPerformed
        // TODO add your handling code here:
        dropCourseTextArea.setText(""); 
        String courseCode = adminDropCourseComboBox.getSelectedItem().toString();
        ArrayList<ScheduleEntry> scheduledStudents = ScheduleQueries.getScheduledStudentsByCourse(currentSemester, courseCode);
        ArrayList<ScheduleEntry> waitlistedStudents = ScheduleQueries.getWaitlistedStudentsByCourse(currentSemester, courseCode);
        
        dropCourseTextArea.append("Scheduled Students Dropped From " + courseCode + "\n");
        for (ScheduleEntry schedule: scheduledStudents) {
            StudentEntry student = StudentQueries.getStudent(schedule.getStudentID());
            dropCourseTextArea.append(student.getLastName() + ", " + student.getFirstName() + " " + student.getStudentID() + "\n");
            ScheduleQueries.dropStudentScheduleByCourse(currentSemester, schedule.getStudentID(), courseCode); 
        }
        
        dropCourseTextArea.append("\nWaitlisted Students Dropped From " + courseCode + "\n");
        for (ScheduleEntry schedule: waitlistedStudents) {
            StudentEntry student = StudentQueries.getStudent(schedule.getStudentID()); 
            dropCourseTextArea.append(student.getLastName() + ", " + student.getFirstName() + " " + student.getStudentID() + "\n");
            ScheduleQueries.dropStudentScheduleByCourse(currentSemester, schedule.getStudentID(), courseCode); 
        }
        
        CourseQueries.dropCourse(currentSemester, courseCode);
        rebuildCourseComboBox(); 
    }//GEN-LAST:event_adminDropCourseButtonActionPerformed

