    public void rebuildStudentComboBox() {
        ArrayList<StudentEntry> students = StudentQueries.getAllStudents(); 
        
        ArrayList<String> studentsFirstLast = new ArrayList<>(); 
        
        //StudentEntry s = new StudentEntry()
               
        
        for(StudentEntry student : students) {
            String firstLast = student.getLastName() + ',' + student.getFirstName();
            studentsFirstLast.add(firstLast); 
        }
            
        scheduleCoursesStudentComboBox.setModel(new javax.swing.DefaultComboBoxModel(studentsFirstLast.toArray()));
        displayScheduleStudentComboBox.setModel(new javax.swing.DefaultComboBoxModel(studentsFirstLast.toArray()));
        changeStudentComboBox.setModel(new javax.swing.DefaultComboBoxModel(studentsFirstLast.toArray()));
        dropStudentComboBox.setModel(new javax.swing.DefaultComboBoxModel(studentsFirstLast.toArray()));
        
        if (students.size() > 0)
        {
            currentStudentLabel.setText(studentsFirstLast.get(0));
            currentStudent = studentsFirstLast.get(0);
           
        }
        else
        {
            currentStudentLabel.setText("None, add a Student.");
            currentStudent = "None";
        }
        
    }
