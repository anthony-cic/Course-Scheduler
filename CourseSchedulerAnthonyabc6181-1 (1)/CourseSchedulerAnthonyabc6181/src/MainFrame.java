    private void scheduleCoursesSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_scheduleCoursesSubmitButtonActionPerformed
        // TODO add your handling code here:
        String courseCode = selectCourseComboBox.getSelectedItem().toString();
        String status;
        ArrayList<StudentEntry> students = StudentQueries.getAllStudents();
        String studentID = students.get(scheduleCoursesStudentComboBox.getSelectedIndex()).getStudentID();
        String firstName = students.get(scheduleCoursesStudentComboBox.getSelectedIndex()).getFirstName(); 
        String lastName = students.get(scheduleCoursesStudentComboBox.getSelectedIndex()).getLastName();

        if (ScheduleQueries.getScheduledStudentCount(currentSemester, courseCode) < CourseQueries.getCourseSeats(currentSemester, courseCode)) {
            status = "S";
            Timestamp ts = new Timestamp(System.currentTimeMillis());
            ScheduleEntry s = new ScheduleEntry(currentSemester, courseCode, studentID, status, ts);
            ScheduleQueries.addScheduleEntry(s);
            scheduleCourseStatusLabel.setText(lastName + ", " + firstName + " has been scheduled for " + courseCode);
        }
        else
        {
            status = "W";
            Timestamp ts = new Timestamp(System.currentTimeMillis());
            ScheduleEntry s = new ScheduleEntry(currentSemester, courseCode, studentID, status, ts);
            ScheduleQueries.addScheduleEntry(s);
            scheduleCourseStatusLabel.setText(lastName + ", " + firstName +  " has been waitlisted for " + courseCode);
        }
        //ArrayList<ScheduleEntry> schedule = ScheduleQueries.addScheduleEntry(currentSemester, courseCode, studentID, status, timestamp);
    }//GEN-LAST:event_scheduleCoursesSubmitButtonActionPerformed

